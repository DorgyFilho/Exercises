#066 - Dia da Semana

dia = input('Digite o número correspondente ao dia - 1.Dom, 2.Seg, 3.Ter, 4.Qua, 5.Qui, 6.Sex, 7.Sab: ')
if dia == '1':
    print('Domingo')
elif dia == '2':
    print('Segunda')
elif dia == '3':
    print('Terça')
elif dia == '4':
    print('Quarta')
elif dia == '5':
    print('Quinta')
elif dia == '6':
    print('Sexta')
elif dia == '7':
    print('Sábado')
else:
    print('Inválido!')