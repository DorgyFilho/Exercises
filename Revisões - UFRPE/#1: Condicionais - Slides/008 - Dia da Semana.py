#008 - Dia da Semana

dia = int(input('Digite o número correspondente ao dia - 1.Dom, 2-Seg, 3-Ter, 4-Qua, 5-Qui, 6-Sex, 7-Sáb: '))
if dia == 1:
    print('Domingo!')
elif dia == 2:
    print('Segunda-Feira!')
elif dia == 3:
    print('Terça-Feira!')
elif dia == 4:
    print('Quarta-Feira!')
elif dia == 5:
    print('Quinta-Feira!')
elif dia == 6:
    print('Sexta-Feira!')
elif dia == 7:
    print('Sábado!')
else:
    print('Inválido!')
